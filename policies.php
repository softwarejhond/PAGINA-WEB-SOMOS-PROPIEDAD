                <ul>
                  <li>Encriptar la prestación de nuestros servicios usando protocolos de seguridad.</li>
                  <li>Establecimiento de cláusulas de confidencialidad contractual con los empleados que van
                    más allá de la duración misma del contrato.</li>
                  <li>Implementación de procesos de seguridad para verificar la identidad de las personas que
                    acceden a la información ya sea de manera física o electrónica.</li>
                  <li>Actualización permanente de las medidas de seguridad para adaptarlas a la normatividad
                    vigente.</li>
                  <li>Adopción de sistemas de seguridad de firewalls y detección de accesos no autorizados.</li>
                  <li>Monitoreo periódico de actividades sospechosas y mantenimiento físico y electrónico de
                    las bases de datos.</li>
                  <li>Restricción interna de acceso a las bases de datos solo al personal autorizado.</li>
                </ul>
              </li>
              <li>
                <h5>Finalización del Tratamiento</h5>
                <p>Una vez cese la necesidad de tratamiento de sus datos, los mismos podrán ser eliminados
                  de las bases de datos de SOMOS PROPIEDAD S.A.S o archivados en términos seguros a
                  efectos de que solamente sean divulgados cuando a ello hubiere lugar de acuerdo con la
                  ley. Dichos datos no serán eliminados a pesar de la solicitud del titular, cuando la
                  conservación de los mismos sea necesaria para el cumplimiento de una obligación o
                  contrato.</p>
              </li>
