                  la ejecución del contrato, lo que incluye la autorización de recolectar, modificar o corregir,
                  transferir en momentos futuros, datos personales del titular, para efectos tales como las
                  cotizaciones y pagos al sistema de seguridad social, parafiscales, etc. Es un deber de los
                  empleados y colaboradores de SOMOS PROPIEDAD S.A.S conocer esta Política y realizar
                  todos los actos conducentes para su cumplimiento, implementación y mantenimiento.</p>
