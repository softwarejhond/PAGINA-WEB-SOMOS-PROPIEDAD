                    SOMOS PROPIEDAD S.A.S, tendrá como única finalidad verificar la idoneidad moral y
                    competencia de los empleados; por tanto, una vez verificado este requisito, SOMOS
                    PROPIEDAD S.A.S podrá devolver tal información al proveedor, salvo cuando fuere
                    necesario preservar estos datos. SOMOS PROPIEDAD S.A.S verificará que los datos
                    solicitados sean necesarios, pertinentes y no excesivos respecto de la finalidad que
                    fundamente la solicitud de acceso a los mismos.</li>
                  <li>Tratamiento de datos personales en procesos de contratación. Los terceros que en
                    procesos de contratación, alianzas y acuerdos de cooperación con SOMOS PROPIEDAD
                    S.A.S, accedan, usen, traten y/o almacenen datos personales de empleados de SOMOS
                    PROPIEDAD S.A.S y/o de terceros relacionados con dichos procesos contractuales,
                    adoptarán en lo pertinente lo dispuesto en esta norma, así como las medidas de seguridad
                    que le indique SOMOS PROPIEDAD S.A.S según el tipo de dato de carácter personal
                    tratado. SOMOS PROPIEDAD S.A.S verificará que los datos solicitados sean necesarios,
                    pertinentes y no excesivos respecto de la finalidad del tratamiento.</li>
                  <li>Tratamiento de datos personales de la comunidad y clientes. La recolección de datos de
                    personas que SOMOS PROPIEDAD S.A.S trate en desarrollo de acciones relacionadas
                    con la comunidad, se sujetará a lo dispuesto en esta norma y estará orientada a promover
                    los bienes y servicios de SOMOS PROPIEDAD S.A.S, así como para realizar campañas de
                    responsabilidad social. Para el efecto, previamente SOMOS PROPIEDAD S.A.S informará
                    y obtendrá la autorización de los titulares de los datos. En cuanto a los clientes, además de
                    tener como finalidad la promoción y comercialización de los bienes y servicios de la
                    compañía, el tratamiento se realizará fundamentalmente para realizar todos los trámites
                    necesarios para la compraventa de las viviendas y los procesos derivados de dicho negocio
                    conforme se describe en el manual de tratamiento de datos.</li>
                </ul>
