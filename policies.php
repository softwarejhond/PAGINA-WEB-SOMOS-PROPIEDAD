              <li>
                <h5>Principios para el Tratamiento de Datos Personales</h5>
                <p>SOMOS PROPIEDAD SAS incorpora en todas sus actuaciones el respeto por la protección
                  de datos personales y, por consiguiente, se regirá por los siguientes principios:</p>
                <ul>
                  <li>Principio de Legalidad en materia de Tratamiento de datos: El tratamiento de datos es una
                    actividad reglada que debe sujetarse a lo establecido en la ley y en las demás disposiciones
                    que la desarrollen.</li>
                  <li> Principio de Finalidad: El tratamiento debe obedecer a una finalidad legítima de acuerdo
                    con la Constitución Política y la ley, la cual debe ser informada al Titular.</li>
                  <li> Principio de Libertad: El tratamiento sólo puede ejercerse con el consentimiento, previo,
                    expreso e informado del Titular. Los datos personales no podrán ser obtenidos o divulgados
                    sin previa autorización, o en ausencia de mandato legal o judicial que releve el
                    consentimiento.</li>
                  <li>Principio de Veracidad o Calidad: La información sujeta a tratamiento debe ser veraz,
                    completa, exacta, actualizada, comprobable y comprensible. Se prohíbe el tratamiento de
                    datos parciales, incompletos, fraccionados o que induzcan a error.</li>
                  <li>Principio de Transparencia: En el tratamiento debe garantizarse el derecho del Titular a
                    obtener del responsable de dicho tratamiento o del Encargado, en cualquier momento y sin
                    restricciones, información acerca de la existencia de datos que le conciernan.</li>
                  <li> Principio de Acceso y Circulación Restringida: El tratamiento se sujeta a los límites que se
                    derivan de la naturaleza de los datos personales y de las disposiciones constitucionales y
                    legales. En este sentido, el tratamiento sólo podrá hacerse por personas autorizadas por el
                    Titular y/o por las personas previstas en la Ley. Los datos personales, salvo la información
                    pública, no podrán estar disponibles en Internet u otros medios de divulgación o
                    comunicación masiva, salvo que el acceso sea técnicamente controlable para brindar un
                    conocimiento restringido sólo a los Titulares o terceros autorizados conforme a la citada ley.</li>
                  <li>Principio de Seguridad: La información sujeta a tratamiento por el responsable del
                    Tratamiento o Encargado del tratamiento a que se refiere la ley se deberá manejar con las
                    medidas técnicas, humanas y administrativas que sean necesarias para otorgar seguridad
                    a los registros, y evitar su adulteración, pérdida, consulta, uso o acceso no autorizado o
                    fraudulento.</li>
                  <li>Principio de Confidencialidad: Todas las personas que intervengan en el tratamiento de
                    datos personales que no tengan la naturaleza de públicos están obligadas a garantizar la
                    reserva de la información, inclusive después de finalizada su relación con alguna de las
                    labores que comprende el tratamiento, pudiendo sólo realizar suministro o comunicación de
                    datos personales cuando ello corresponda al desarrollo de las actividades autorizadas en
                    la Ley y en los términos de la misma.</li>
                </ul>
