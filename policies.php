                    entregará a las personas no seleccionadas los datos personales suministrados, salvo que
                    los titulares de los datos por escrito autoricen la destrucción de los mismos, cuando el titular
                    del dato no sea seleccionado. La información obtenida por SOMOS PROPIEDAD S.A.S
                    respecto de quienes no fueron seleccionados, resultados de las pruebas sicotécnicas y
                    entrevistas, serán eliminados de sus sistemas de información, dando así cumplimiento al
                    principio de finalidad. SOMOS PROPIEDAD S.A.S cuando contrate procesos de selección
                    de personal con terceros regulará en los contratos el tratamiento que se deberá dar a los
                    datos personales entregados por los interesados, así como la destinación de la información
                    personal obtenida del respectivo proceso. Los datos personales e información obtenida del
                    proceso de selección respecto del personal seleccionado para laborar en SOMOS
                    PROPIEDAD S.A.S, serán almacenados en la carpeta personal, aplicando a esta
                    información niveles y medidas de seguridad altas, en virtud de la potencialidad de que tal
                    información contenga datos de carácter sensible. La finalidad de la entrega de los datos
                    suministrados por los interesados en las vacantes de SOMOS PROPIEDAD S.A.S y la
                    información personal obtenida del proceso de selección, se limita a la participación en el
                    mismo; por tanto, su uso para fines diferentes está prohibido. SOMOS PROPIEDAD S.A.S
                    almacenará los datos personales e información personal obtenida del proceso de selección
                    de los empleados en una carpeta identificada con el nombre de cada uno de ellos. Esta
                    carpeta física o digital solo será accedida y tratada por el Área de Recursos Humanos y con
                    la finalidad de administrar la relación contractual entre SOMOS PROPIEDAD S.A.S y el
                    empleado. El uso diferente de los datos e información personal de los empleados solo
